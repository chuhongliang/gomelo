func isContextParam(field *ast.Field) bool {
	if field.Type == nil {
		return false
	}

	switch t := field.Type.(type) {
	case *ast.Ident:
		return t.Name == "Context"
	case *ast.StarExpr:
		if sel, ok := t.X.(*ast.SelectorExpr); ok {
			if sel.Sel != nil && sel.Sel.Name == "Context" {
				return true
			}
		}
	case *ast.SelectorExpr:
		if t.Sel != nil && t.Sel.Name == "Context" {
			return true
		}
	}
	return false
}

func generate(output string, types []ServerType, basePath string) error {
	t, err := template.New("servers_gen").Parse(genTmpl)
	if err != nil {
		return err
	}

	imports := buildImports(types, basePath)
	importMap := make(map[string]string)
	for _, imp := range imports {
		importMap[imp.Path] = imp.Alias
	}

	data := GenData{
		Types:     types,
		Imports:   imports,
		ImportMap: importMap,
	}

	var buf bytes.Buffer
	if err := t.Execute(&buf, data); err != nil {
		return err
	}

	return os.WriteFile(output, buf.Bytes(), 0644)
}

func buildImports(types []ServerType, basePath string) []ImportInfo {
	seen := make(map[string]bool)
	var imports []ImportInfo

	moduleName := getModuleName()

	for _, st := range types {
		for _, h := range st.Handlers {
			if h.ImportPath == "" {
				continue
			}
			fullPath := resolveImportPath(h.ImportPath, basePath, moduleName)
			if seen[fullPath] {
				continue
			}
			seen[fullPath] = true
			alias := h.Package + "_" + st.Name
			imports = append(imports, ImportInfo{
				Alias: alias,
				Path:  fullPath,
			})
		}
		for _, r := range st.Remotes {
			if r.ImportPath == "" {
				continue
			}
			fullPath := resolveImportPath(r.ImportPath, basePath, moduleName)
			if seen[fullPath] {
				continue
			}
			seen[fullPath] = true
			alias := r.Package + "_" + st.Name
			imports = append(imports, ImportInfo{
				Alias: alias,
				Path:  fullPath,
			})
		}
		for _, f := range st.Filters {
			if f.ImportPath == "" {
				continue
			}
			fullPath := resolveImportPath(f.ImportPath, basePath, moduleName)
			if seen[fullPath] {
				continue
			}
			seen[fullPath] = true
			alias := f.Package + "_" + st.Name
			imports = append(imports, ImportInfo{
				Alias: alias,
				Path:  fullPath,
			})
		}
		for _, c := range st.Crons {
			if c.ImportPath == "" {
				continue
			}
			fullPath := resolveImportPath(c.ImportPath, basePath, moduleName)
			if seen[fullPath] {
				continue
			}
			seen[fullPath] = true
			alias := c.Package + "_" + st.Name
			imports = append(imports, ImportInfo{
				Alias: alias,
				Path:  fullPath,
			})
		}
	}

	return imports
}

func getModuleName() string {
	data, err := os.ReadFile("go.mod")
	if err != nil {
		return ""
	}
	lines := strings.Split(string(data), "\n")
	if len(lines) > 0 && strings.HasPrefix(lines[0], "module ") {
		return strings.TrimPrefix(lines[0], "module ")
	}
	return ""
}

func resolveImportPath(relPath string, basePath string, moduleName string) string {
	relPath = strings.TrimPrefix(relPath, "./")
	relPath = strings.TrimPrefix(relPath, ".")
	if moduleName == "" {
		return relPath
	}
	absBase, err := filepath.Abs(basePath)
	if err != nil {
		return relPath
	}
	cwd, err := os.Getwd()
	if err != nil {
		return relPath
	}
	rel, err := filepath.Rel(cwd, absBase)
	if err != nil {
		return relPath
	}
	rel = strings.TrimPrefix(rel, ".")
	rel = strings.TrimPrefix(rel, string(filepath.Separator))
	rel = filepath.ToSlash(rel)
	relPath = filepath.ToSlash(relPath)

	baseName := filepath.Base(basePath)
	relPath = strings.TrimPrefix(relPath, baseName+"/")

	return moduleName + "/" + rel + "/" + relPath
}
